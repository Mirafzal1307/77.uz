
let popup = document.getElementById("popup");
let div = document.getElementById("div");

function openPopup(){
    popup.classList.add("open-popup");
    div.classList.add("active");
}

function closePopup(){
    popup.classList.remove("open-popup");
    div.classList.remove("active");
}



